name = 'Salman Irfan'

# length
lenght = len(name)
print (lenght)

# lower
lower_name = name.lower()
print (lower_name) # salman irfan

# upper
upper_name = name.upper()
print (upper_name) # SALMAN IRFAN

# title
title_name = name.title()
print (title_name) # Salman Irfan

# count
count_name = name.count("a")
print (count_name) # 3