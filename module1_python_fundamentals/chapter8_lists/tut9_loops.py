# Define a list of fruits
fruits = ["apple", "banana", "orange", "kiwi", "grape"]

# Using a for loop to iterate over the fruits list
print("Using a for loop:")
for fruit in fruits:
    print(fruit)

# Using a while loop to iterate over the fruits list
print("\nUsing a while loop:")
index = 0
while index < len(fruits):
    print(fruits[index])
    index += 1
